#!/usr/bin/env node
// build-contract-artifacts.mjs - compile every contracts/*.sol exactly the way
// its deploy script does (solc, optimizer runs=200, viaIR only for the
// catalog) and emit the results where the browser can use them:
//
//   site/js/gen/contract-artifacts.js   ES module: { abi, bytecode, selectors,
//                                       constructor inputs, address-book key }
//                                       per contract - the admin console
//                                       (site/admin.html) deploys and calls
//                                       owner functions from this, so nobody
//                                       pastes a private key into a terminal.
//   contracts/<Name>.abi.json           kept in sync for all six (the deploy
//                                       scripts re-emit five of them; EnclavePay
//                                       never had one until this script).
//
// The generated module is COMMITTED (the dev flow serves site/ unbundled), and
// scripts/build-site.mjs re-runs this before every bundle so it cannot go
// stale against the .sol sources. Selectors come from viem, not by hand -
// hand-computed selectors have burned us before.
//
// Deps (repo root): npm i solc viem      Usage: node scripts/build-contract-artifacts.mjs
import fs from "node:fs";
import path from "node:path";
import url from "node:url";
import solc from "solc";
import { toFunctionSelector, toEventSelector } from "viem";

const REPO = path.resolve(path.dirname(url.fileURLToPath(import.meta.url)), "..");
const OUT = path.join(REPO, "site", "js", "gen", "contract-artifacts.js");

// name -> its EnclaveAddressBook key (null = the book itself has no entry) and
// whether its deploy script compiles viaIR (publishVersion's 7 params overflow
// legacy codegen - mirror deploy-app-catalog.mjs or the bytecode differs).
const DEFS = [
  { name: "EnclaveAddressBook",  bookKey: null },
  { name: "EnclaveRegistry",     bookKey: "registry" },
  { name: "EnclaveAppCatalog",   bookKey: "appCatalog", viaIR: true },
  { name: "EnclavePay",          bookKey: "enclavePay" },
  { name: "EnclaveDeployments",  bookKey: "deployments" },
  { name: "EnclaveFeatured",     bookKey: "featured" },
  { name: "EnclaveReviews",      bookKey: "reviews" },
  { name: "PaymentRouter",       bookKey: "paymentRouter" },
];

function compile(name, viaIR) {
  const file = name + ".sol";
  const source = fs.readFileSync(path.join(REPO, "contracts", file), "utf8");
  const input = {
    language: "Solidity",
    sources: { [file]: { content: source } },
    settings: {
      optimizer: { enabled: true, runs: 200 },
      ...(viaIR ? { viaIR: true } : {}),
      outputSelection: { "*": { "*": ["abi", "evm.bytecode.object"] } },
    },
  };
  const out = JSON.parse(solc.compile(JSON.stringify(input)));
  const errs = (out.errors || []).filter((e) => e.severity === "error");
  if (errs.length) throw new Error(`solc(${file}):\n` + errs.map((e) => e.formattedMessage).join("\n"));
  const c = out.contracts[file][name];
  if (!c || !c.evm.bytecode.object) throw new Error(`no bytecode for ${name}`);
  return { abi: c.abi, bytecode: "0x" + c.evm.bytecode.object };
}

const artifacts = {};
for (const def of DEFS) {
  const { abi, bytecode } = compile(def.name, def.viaIR);
  fs.writeFileSync(path.join(REPO, "contracts", def.name + ".abi.json"),
    JSON.stringify(abi, null, 2) + "\n");
  const sel = {};
  for (const f of abi) {
    if (f.type !== "function") continue;
    if (sel[f.name]) throw new Error(`${def.name}.${f.name} is overloaded - selector map needs qualified names`);
    sel[f.name] = toFunctionSelector(f).slice(2);   // bare 8-hex, the site's convention (chain.js SEL maps)
  }
  const evt = {};
  for (const e of abi) if (e.type === "event") evt[e.name] = toEventSelector(e);
  const ctorAbi = abi.find((f) => f.type === "constructor");
  artifacts[def.name] = {
    bookKey: def.bookKey,
    ctor: (ctorAbi ? ctorAbi.inputs : []).map((i) => ({ name: i.name.replace(/^_/, ""), type: i.type })),
    sel,
    evt,
    bytecode,
  };
  console.log(`  ${def.name.padEnd(21)} ${(bytecode.length / 2 - 1).toString().padStart(6)} bytes · ${Object.keys(sel).length} fns${def.viaIR ? " · viaIR" : ""}`);
}

const banner = `/* ============================================================
   GENERATED by scripts/build-contract-artifacts.mjs - DO NOT EDIT.
   Compiled from contracts/*.sol with the same solc settings the
   deploy scripts use (optimizer runs=200; viaIR for the catalog).
   Loaded ONLY by the admin console (a code-split chunk of
   admin.html): bytecode for browser-wallet deploys, viem-derived
   selectors (bare 8-hex) for owner calls, constructor metadata
   for the deploy forms, and each contract's address-book key.
   Regenerate: node scripts/build-contract-artifacts.mjs
   ============================================================ */
`;
fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, banner + "export const CONTRACTS = " + JSON.stringify(artifacts, null, 2) + ";\n");
console.log(`[artifacts] wrote ${path.relative(REPO, OUT)} (${(fs.statSync(OUT).size / 1024).toFixed(1)} KB) + ${DEFS.length} abi.json`);
